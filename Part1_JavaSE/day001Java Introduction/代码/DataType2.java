/*

常量： 固定不变的量 常量



*/

class DataType2{

	public static void main(String [] args){
	
		//字符串常量
		System.out.println("HelloWorld");
		//浮点类型常量
		System.out.println(3.14);
		//字符常量
		System.out.println('你');
		//布尔类型常量
	    System.out.println(true);
		//java内置的常量 PI
	    System.out.println(Math.PI);
	}
	
}